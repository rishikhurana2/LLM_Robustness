#!/usr/bin/env python3
"""
Generate benchmark charts from one or more model JSON files.

Expected input format:
- directory of JSON files that contain the categories generated by running main.py 

Outputs:
- overall_accuracy_by_model.png -- overall accuracy on questions.json across models
- by_category/<category>.png -- Histograms for each category, showing % correct across models for that category
- model_stacked/<model>.png  -- Stacked bar char showing % correct and % wrong for each category (one for each model)
- CSV summaries

Example:
    python generate_results.py --input_dir ../Answers/Answers-FRQ --output_dir ../Results/Results-FRQ
"""

from __future__ import annotations

import argparse
import json
import re
from pathlib import Path
from typing import Any

import matplotlib.pyplot as plt
import pandas as pd

TOP_LEVEL_LIST_KEYS = ["results", "data", "items", "records"]

def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Generate benchmark graphs from model JSON files.")
    parser.add_argument(
        "--input_dir",
        type=str,
        required=True,
        help="Directory containing model JSON files.",
    )
    parser.add_argument(
        "--output_dir",
        type=str,
        default="benchmark_graphs",
        help="Directory to save graphs and CSV summaries.",
    )
    return parser.parse_args()

def load_json_records(path: Path) -> list[dict[str, Any]]:
    with path.open("r", encoding="utf-8") as f:
        data = json.load(f)

    if isinstance(data, list):
        records = data
    elif isinstance(data, dict):
        records = None
        for key in TOP_LEVEL_LIST_KEYS:
            value = data.get(key)
            if isinstance(value, list):
                records = value
                break
        if records is None:
            raise ValueError(
                f"{path.name}: JSON must be a list or contain a top-level list under one of {TOP_LEVEL_LIST_KEYS}."
            )
    else:
        raise ValueError(f"{path.name}: Unsupported JSON structure.")

    cleaned: list[dict[str, Any]] = []
    for i, record in enumerate(records):
        if not isinstance(record, dict):
            raise ValueError(f"{path.name}: Record {i} is not a JSON object.")
        row = dict(record)
        row["_source_file"] = path.name
        row["_row_index"] = i
        cleaned.append(row)
    return cleaned

def normalize_correct(value: Any) -> bool | None:
    """Convert a Correct field to True/False, or None if missing/blank/unparseable."""
    if value is None:
        return None

    try:
        if pd.isna(value):
            return None
    except Exception:
        pass

    if isinstance(value, bool):
        return value

    if isinstance(value, (int, float)):
        if value in (0, 0.0):
            return False
        if value in (1, 1.0):
            return True
        return None

    if isinstance(value, str):
        v = value.strip().lower()
        if v in {"", "none", "null", "nan", "n/a", "na", "unknown"}:
            return None
        if v in {"true", "t", "yes", "y", "1", "correct"}:
            return True
        if v in {"false", "f", "no", "n", "0", "wrong", "incorrect"}:
            return False
        return None

    return None

def normalize_category(cat: Any) -> str:
    if cat is None:
        return "Unknown"

    try:
        if pd.isna(cat):
            return "Unknown"
    except Exception:
        pass

    cat = str(cat).strip()
    if not cat:
        return "Unknown"

    cat = re.sub(r"\s+", " ", cat)
    lower = cat.lower()

    if lower.startswith("spatial relation"):
        return "Spatial Relations"
    if lower.startswith("spatial visualization"):
        return "Spatial Visualization"
    if lower.startswith("spatial counting"):
        return "Spatial Counting"
    if lower.startswith("logic"):
        return "Logic"
    if lower.startswith("commonsense"):
        return "Commonsense"

    return cat

def slugify(text: str) -> str:
    text = text.strip().lower()
    text = re.sub(r"[^a-z0-9]+", "_", text)
    text = re.sub(r"_+", "_", text).strip("_")
    return text or "untitled"

def read_all_jsons(input_dir: Path) -> pd.DataFrame:
    json_files = sorted(input_dir.glob("*.json"))
    if not json_files:
        raise FileNotFoundError(f"No JSON files found in {input_dir}")

    all_records: list[dict[str, Any]] = []
    for path in json_files:
        all_records.extend(load_json_records(path))

    df = pd.DataFrame(all_records)
    if df.empty:
        raise ValueError("No records found in the input JSON files.")

    required_cols = {"Correct", "Category"}
    missing = required_cols - set(df.columns)
    if missing:
        raise ValueError(f"Missing required columns across input files: {sorted(missing)}")

    if "Model" not in df.columns:
        df["Model"] = df["_source_file"].map(lambda x: Path(x).stem)

    df["Model"] = df["Model"].fillna(df["_source_file"].map(lambda x: Path(x).stem)).astype(str).str.strip()
    df["Model"] = df["Model"].replace("", pd.NA).fillna(df["_source_file"].map(lambda x: Path(x).stem))

    df["Correct_bool"] = df["Correct"].map(normalize_correct)
    df["Category_norm"] = df["Category"].map(normalize_category)

    invalid_mask = df["Correct_bool"].isna()
    invalid_count = int(invalid_mask.sum())
    if invalid_count:
        print(f"Warning: skipping {invalid_count} row(s) with missing or unparseable Correct values.")
        invalid_by_file = df.loc[invalid_mask, "_source_file"].value_counts().sort_index()
        for source_file, count in invalid_by_file.items():
            print(f"  - {source_file}: {count} skipped row(s)")

    df = df.loc[~invalid_mask].copy()
    if df.empty:
        raise ValueError("All rows were skipped because Correct values were missing or unparseable.")

    df["Correct_bool"] = df["Correct_bool"].astype(bool)
    df["Outcome"] = df["Correct_bool"].map(lambda x: "Right" if x else "Wrong")
    return df

def ensure_dirs(base_output: Path) -> dict[str, Path]:
    dirs = {
        "base": base_output,
        "by_category": base_output / "by_category",
        "model_stacked": base_output / "model_stacked",
        "csv": base_output / "csv",
    }
    for d in dirs.values():
        d.mkdir(parents=True, exist_ok=True)
    return dirs



def save_overall_accuracy(df: pd.DataFrame, out_dir: Path) -> pd.DataFrame:
    summary = (
        df.groupby("Model", as_index=False)["Correct_bool"]
        .mean()
        .rename(columns={"Correct_bool": "accuracy"})
    )
    counts = df.groupby("Model", as_index=False).size().rename(columns={"size": "n"})
    summary = summary.merge(counts, on="Model", how="left")
    summary["percent_correct"] = summary["accuracy"] * 100
    summary = summary.sort_values("percent_correct", ascending=False)

    plt.figure(figsize=(10, 6))
    plt.bar(summary["Model"], summary["percent_correct"])
    plt.ylabel("% Correct")
    plt.xlabel("Model")
    plt.title("Overall % Correct by Model")
    plt.ylim(0, 100)
    plt.xticks(rotation=30, ha="right")

    for i, row in summary.reset_index(drop=True).iterrows():
        plt.text(i, row["percent_correct"] + 1, f"{row['percent_correct']:.1f}%\n(n={int(row['n'])})", ha="center", va="bottom", fontsize=9)

    plt.tight_layout()
    plt.savefig(out_dir / "overall_accuracy_by_model.png", dpi=200, bbox_inches="tight")
    plt.close()
    return summary

def save_category_accuracies(df: pd.DataFrame, out_dir: Path) -> pd.DataFrame:
    summary = (
        df.groupby(["Category_norm", "Model"], as_index=False)["Correct_bool"]
        .mean()
        .rename(columns={"Correct_bool": "accuracy"})
    )
    counts = df.groupby(["Category_norm", "Model"], as_index=False).size().rename(columns={"size": "n"})
    summary = summary.merge(counts, on=["Category_norm", "Model"], how="left")
    summary["percent_correct"] = summary["accuracy"] * 100

    for category, cat_df in summary.groupby("Category_norm"):
        cat_df = cat_df.sort_values("percent_correct", ascending=False).reset_index(drop=True)

        plt.figure(figsize=(10, 6))
        plt.bar(cat_df["Model"], cat_df["percent_correct"])
        plt.ylabel("% Correct")
        plt.xlabel("Model")
        plt.title(f"% Correct by Model — {category}")
        plt.ylim(0, 100)
        plt.xticks(rotation=30, ha="right")

        for i, row in cat_df.iterrows():
            plt.text(i, row["percent_correct"] + 1, f"{row['percent_correct']:.1f}%\n(n={int(row['n'])})", ha="center", va="bottom", fontsize=9)

        plt.tight_layout()
        filename = f"{slugify(category)}_accuracy_by_model.png"
        plt.savefig(out_dir / filename, dpi=200, bbox_inches="tight")
        plt.close()

    return summary

def save_model_stacked_bars(df: pd.DataFrame, out_dir: Path) -> pd.DataFrame:
    category_order = [
        "Commonsense",
        "Logic",
        "Nonsense",
        "Spatial Counting",
        "Spatial Relations",
        "Spatial Visualization",
        "Visual Text Understanding",
    ]

    display_labels = [
        "Commonsense",
        "Logic",
        "Nonsense",
        "Spatial\nCounting",
        "Spatial\nRelations",
        "Spatial\nVisualization",
        "Visual Text\nUnderstanding",
    ]

    summary = (
        df.groupby(["Model", "Category_norm", "Outcome"], as_index=False)
        .size()
        .rename(columns={"size": "count"})
    )

    pivot = (
        summary.pivot_table(
            index=["Model", "Category_norm"],
            columns="Outcome",
            values="count",
            fill_value=0,
        )
        .reset_index()
    )

    if "Right" not in pivot.columns:
        pivot["Right"] = 0
    if "Wrong" not in pivot.columns:
        pivot["Wrong"] = 0

    pivot["n"] = pivot["Right"] + pivot["Wrong"]
    pivot["right_pct"] = (pivot["Right"] / pivot["n"] * 100).fillna(0)
    pivot["wrong_pct"] = (pivot["Wrong"] / pivot["n"] * 100).fillna(0)

    for model, model_df in pivot.groupby("Model"):
        # Reorder categories exactly as requested
        model_df = (
            model_df.set_index("Category_norm")
            .reindex(category_order)
            .reset_index()
        )

        # Fill any missing categories with zeros
        model_df["Model"] = model
        model_df[["Right", "Wrong", "n", "right_pct", "wrong_pct"]] = (
            model_df[["Right", "Wrong", "n", "right_pct", "wrong_pct"]].fillna(0)
        )

        plt.figure(figsize=(23, 7))

        # Add more horizontal spacing between bars
        x_positions = [i * 1.35 for i in range(len(model_df))]

        # Slightly narrower bars to create visible gaps
        plt.bar(x_positions, model_df["right_pct"], width=0.75)

        fontsize = 30

        plt.xticks(
            x_positions,
            display_labels,
            rotation=0,
            ha="center",
            fontsize=fontsize,
        )
        plt.yticks(fontsize=14)

        plt.ylabel("Accuracy (%)", fontsize=fontsize + 2)
        plt.xlabel("Category", fontsize=fontsize + 4, labelpad=18)
        plt.title(
            f"Accuracy by Category — {model}",
            fontweight="normal",
            fontsize=fontsize + 4,
            pad=34,
        )

        # Extra room above 100% so labels don't hit the title
        plt.ylim(0, 100)

        plt.grid(axis="y", linestyle="--", alpha=0.6)

        # Put value labels above each bar
        for i, row in model_df.iterrows():
            pct = float(row["right_pct"])
            plt.text(
                x_positions[i],
                pct + 1.5,
                f"{pct:.1f}%",
                ha="center",
                va="bottom",
                fontsize=fontsize - 9,
            )

        # Extra bottom margin for wrapped x-axis labels
        plt.subplots_adjust(bottom=0.18)

        plt.tight_layout()
        plt.savefig(
            out_dir / f"{slugify(model)}_category_accuracy_histogram.png",
            dpi=200,
            bbox_inches="tight",
        )
        plt.close()

    return pivot[["Model", "Category_norm", "Right", "Wrong", "n", "right_pct", "wrong_pct"]]

def save_csvs(overall: pd.DataFrame, category: pd.DataFrame, stacked: pd.DataFrame, out_dir: Path) -> None:
    overall.to_csv(out_dir / "overall_accuracy_by_model.csv", index=False)
    category.to_csv(out_dir / "accuracy_by_model_and_category.csv", index=False)
    stacked.to_csv(out_dir / "stacked_breakdown_by_model_category_outcome.csv", index=False)

def main() -> None:
    args = parse_args()
    input_dir = Path(args.input_dir)
    output_dir = Path(args.output_dir)

    if not input_dir.exists() or not input_dir.is_dir():
        raise NotADirectoryError(f"Input directory does not exist or is not a directory: {input_dir}")

    dirs = ensure_dirs(output_dir)
    df = read_all_jsons(input_dir)

    overall = save_overall_accuracy(df, dirs["base"])
    category = save_category_accuracies(df, dirs["by_category"])
    stacked = save_model_stacked_bars(df, dirs["model_stacked"])
    save_csvs(overall, category, stacked, dirs["csv"])

    print("Saved graphs to:", output_dir.resolve())
    print("Files created:")
    print("- overall_accuracy_by_model.png")
    print("- by_category/*.png")
    print("- model_stacked/*.png")
    print("- csv/*.csv")


if __name__ == "__main__":
    main()
